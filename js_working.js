/*
    Execution Context:- Every thing in Javascript happens inside an execution context. It is basically like a container 
        in which whole Javascript code is executed. 
        It is divided into two parts:-
        1. Memory Component (Variable Environment): It is the component where the variables and functions are stored as key value pairs.
        2. Code Component (Thread of Execution): It is the component where the whole execution of code takes place. 
            It is the place in which code executes one line at a time. 
    Note:- JS is synchronous single-threaded language that means in this single thread or a single command executes at a time
        and in a specific order means it can only go to the next line once the current line has been finished executing.
*/
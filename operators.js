// Operators and Expression
// Expression - A fragment of code that produces a value is called an expression. Every value written literally is an expression. Ex: 77 or "Prashant"
// Operators - Operators are the symbols that operates the operands
/*
There are various types of operators :
1. Arithmetic Operators: These are used to perform arithmetic calculations. Ex: +, -, *, /, %, **(used for power).
2. Assignment Operators: These operators are used to store the result in a variable. Ex: =, +=, etc. 
3. Logical Operators: These are used to perform logical operations. Ex: &&, ||, !, etc.
4. Comparison Operators: These are used to compare two expressions or conditions. Ex: >, <, >=, <=, !=, etc.
5. Increment/Decrement Operators: These are used for incrementing or decrementing value. Ex: ++, --, etc.
Note Question:
Difference between == and ===:
== is used to provide the result on the basis of comparision of two values
=== is used to provide the result on the basis of comparision of two values as well as by comparing the types of both values
*/ 